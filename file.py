
'''Basic Arithmetic Operations
Overview of Basic Arithmetic Operations in
Python'''

# Addition 
result = 5 + 3
print(result) 
# Output: 8

# Subtraction

result = 10 - 4
print(result) 
 # Output: 6

# Multiplication

result = 2 * 7
print(result)
  # Output: 14

# Division

result = 20 / 4
print(result) 
 # Output: 5.0 (Note: In Python 3, division always returns a float

""" Exponentiation
Exponentiation is a powerful operation that involves raising a number to acertain power."""

result = 2 ** 3
print(result) 
 # Output: 8


"""Modulo Operation
The modulo operation returns the remainder when one number is divided by
another"""

result = 15 % 7
print(result)  # Output: 1


